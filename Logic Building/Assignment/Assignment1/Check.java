class Positive{
          public static void main(String args[]){
          int n1 = 17;
          if(n1>0){
                   System.out.println("Number is positive");
                  }
          else{
               System.out.println("Number is not positive"); 
              }  
          }
}

class Negative{
          public static void main(String args[]){
          int n1 = -23;
          if(n1<0){
                   System.out.println("Number is negative");
                  }
          else{
               System.out.println("Number is not negative"); 
              }  
          }
}

