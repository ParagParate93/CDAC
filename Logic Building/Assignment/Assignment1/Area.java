class square{
             public static void main(String args[]){
             int side = 4;
             int area = side*side;
             System.out.println("Area of square is: " + area);
}
}

class rectangle{
             public static void main(String args[]){
             int length = 4;
             int width = 8;
             int area = length*width;
             System.out.println("Area of rectangle is: " + area);
}
}